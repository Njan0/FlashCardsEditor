    private void jButtonOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOpenActionPerformed
        // open file dialog
        JFileChooser chooser = new JFileChooser(currentPath); 
        chooser.setDialogTitle(SELECT_SET_TITLE);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("XML", "xml");
        chooser.setFileFilter(filter);
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            // update path text field
            String path = chooser.getSelectedFile().toString();
            jTextFieldPath.setText(path);

            // check if file exists
            File file = new File(path);
            if (!file.exists()) {
                JOptionPane.showMessageDialog(null, "Invalid path: " + path, "File not found!", JOptionPane.ERROR_MESSAGE);
                jTextFieldPath.setText(currentPath);
                return;
            }

            currentPath = path;
            
            // load card set
            CardSet set = new CardSet(file);
            jTextFieldSetName.setText(set.getName());
            jFormattedTextFieldTextSize.setValue(set.getTextSize());
            
            // load each card to ComboBox
            jComboBoxCard.removeAllItems();
            set.getCards().forEach(card -> {
                jComboBoxCard.addItem(card);
            });
        }
    }//GEN-LAST:event_jButtonOpenActionPerformed

    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        save();
    }//GEN-LAST:event_jButtonSaveActionPerformed

    /**
     * Save current card set.
     */
    private void save() {
        // get all cards in ComboBox
        List<Card> cards = new ArrayList<>();
        for (int i = 0; i < jComboBoxCard.getItemCount(); ++i) {
            cards.add(jComboBoxCard.getItemAt(i));
        }
        
        // save to xml file
        File file = new File(jTextFieldPath.getText());
        new CardSet(jTextFieldSetName.getText(), getTextSizeValue(), cards).save(file);
    }
    
    /**
     * Get text size from corresponding text field
     * @return text size
     */
    private float getTextSizeValue() {
        // if textfield empty set to default value
        if (jFormattedTextFieldTextSize.getText().equals("")) {
            jFormattedTextFieldTextSize.setValue(CardSet.DEFAULT_TEXT_SIZE);
        }
        return Float.valueOf(jFormattedTextFieldTextSize.getText());
    }
    
