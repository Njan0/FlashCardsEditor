    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // remove currently selected card
        int index = jComboBoxCard.getSelectedIndex();
        if (index != -1) {
            jComboBoxCard.removeItemAt(index);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jComboBoxCardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxCardActionPerformed
        Card card = (Card) jComboBoxCard.getSelectedItem();
        if (card != null) {
            // set textfields corresponding to card selected
            jTextAreaFront.setText(card.front);
            jTextAreaBack.setText(card.back);
            jTextFieldID.setText(card.id);
            
            // enable components when a card is selected
            jTextAreaFront.setEnabled(true);
            jTextAreaBack.setEnabled(true);
            jTextFieldID.setEnabled(true);
            jButtonDelete.setEnabled(true);
            jButtonApply.setEnabled(true);
        } else {
            // empty textfield
            jTextAreaFront.setText("");
            jTextAreaBack.setText("");
            jTextFieldID.setText("");
            
            // disable components when no card selected
            jTextAreaFront.setEnabled(false);
            jTextAreaBack.setEnabled(false);
            jTextFieldID.setEnabled(false);
            jButtonDelete.setEnabled(false);
            jButtonApply.setEnabled(false);
        }
    }//GEN-LAST:event_jComboBoxCardActionPerformed

    private void jButtonApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApplyActionPerformed
        // apply changes to card
        Card card = (Card)jComboBoxCard.getSelectedItem();
        card.front = jTextAreaFront.getText();
        card.back = jTextAreaBack.getText();
        card.id = jTextFieldID.getText();
        
        // Update ComboBox to show changes
        jComboBoxCard.updateUI();
    }//GEN-LAST:event_jButtonApplyActionPerformed

    private void jButtonSaveAsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveAsActionPerformed
        // choose file to save
        JFileChooser chooser = new JFileChooser(currentPath); 
        chooser.setDialogTitle(SAVE_SET_TITLE);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("XML", "xml");
        chooser.setFileFilter(filter);
        if (chooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            // update text field and save
            String path = chooser.getSelectedFile().toString();
            jTextFieldPath.setText(path);
            save();
        }
    }//GEN-LAST:event_jButtonSaveAsActionPerformed

